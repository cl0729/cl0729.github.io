  // 生成丰富的物品描述 - 按照用户指定模板
  generateDescription: function (
    type,
    damage,
    recognitionResults,
    facilityDescription = "",
  ) {
    // 构建描述段落
    let description = "";

    // 第一部分：设施类型
    description += `设施类型：${type}`;

    // 第二部分：损坏情况
    if (damage !== "无法识别损坏情况") {
      description += `\n损坏情况：${damage}`;
    } else {
      description += `\n损坏情况：暂未识别`;
    }

    // 第三部分：用户描述
    if (facilityDescription) {
      description += `\n用户描述：${facilityDescription}`;
    } else {
      description += `\n用户描述：无`;
    }

    // 第四部分：AI生成的描述
    description += `\n\nAI分析：`;

    // 根据设施类型和损坏情况生成描述
    let aiGeneratedDesc = `该${type}`;

    if (damage !== "无法识别损坏情况") {
      aiGeneratedDesc += `出现${damage}`;

      // 根据损坏类型补充说明
      const damageDetails = {
        损坏: "，影响正常使用",
        缺失: "，部分组件丢失",
        故障: "，功能无法正常运行",
        老化: "，使用年限较长",
        污渍: "，表面有污渍",
        变形: "，结构发生变形",
        堵塞: "，存在堵塞",
        漏水: "，有漏水现象",
        噪音: "，产生异常噪音",
        松动: "，连接部位松动",
        倾斜: "，设施发生倾斜",
        褪色: "，表面颜色褪色",
        起皮: "，表面起皮脱落",
        起泡: "，表面起泡",
        开裂: "，出现裂缝",
        剥落: "，表层剥落",
        腐蚀: "，发生腐蚀",
        磨损: "，表面磨损",
        凹陷: "，出现凹陷",
        凸起: "，表面凸起",
      };

      aiGeneratedDesc += damageDetails[damage] || "";
      aiGeneratedDesc += "。";
    } else {
      aiGeneratedDesc += "存在异常情况，具体损坏情况待进一步确认。";
    }

    // 结合用户描述
    if (facilityDescription) {
      aiGeneratedDesc += `用户反馈：${facilityDescription}。`;
    }

    // 添加建议
    aiGeneratedDesc += `建议尽快安排维修人员进行现场查看和处理。`;

    description += aiGeneratedDesc;

    return description;
  },

  // 获取未知结果 - 优化版，更加礼貌
  getUnknownResult: function (facilityDescription = "") {
    let description = "非常感谢您的报修！";

    if (facilityDescription) {
      description += `根据您的描述（${facilityDescription}），`;
    }

    description +=
      "我们暂时无法通过图片准确识别设施类型和损坏情况。不过请您放心，这不会影响我们为您处理报修。建议您在后续表单中尽可能详细地填写设施信息和损坏情况，我们的工作人员会尽快与您联系并前往现场查看。再次感谢您的反馈，祝您生活愉快！";

    return {
      type: "未知设施",
      damage: "无法识别损坏情况",
      confidence: 0.5,
      suggestion: "请补充详细描述，我们会尽快处理",
      description: description,
    };
  },
